import math

def calculator(first_int, math_operator, second_int):
    if math_operator == "+" :
        print(first_int + second_int)
    elif math_operator == "*" :
        print(first_int * second_int)
    elif math_operator == "/" :
        print(first_int / second_int)
    elif math_operator == "-" :
        print(first_int - second_int)




calculator(6, "-", 4)
